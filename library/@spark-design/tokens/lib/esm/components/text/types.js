export var TextSize;
(function (TextSize) {
    TextSize["Large"] = "l";
    TextSize["Medium"] = "m";
    TextSize["Small"] = "s";
    TextSize["ExtraSmall"] = "xs";
})(TextSize || (TextSize = {}));
export var levelMap;
(function (levelMap) {
    levelMap[levelMap["l"] = 100 || 'l'] = "l";
    levelMap[levelMap["m"] = 75 || 'm'] = "m";
    levelMap[levelMap["s"] = 50 || 's'] = "s";
    levelMap[levelMap["xs"] = 25 || 'xs'] = "xs";
})(levelMap || (levelMap = {}));
